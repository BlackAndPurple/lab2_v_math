package v_math;

public class FunctionsKit {

    public static double getSin(double x){
        return Math.sin(x);
    }

    public static double getCos(double x){
        return Math.cos(x);
    }

    public static double getLn(double x){
        return Math.log(x);
    }

    public static  double get2x(double x) {return 2*x;}
}
